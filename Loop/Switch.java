package Com.Sgm.SwitchCase;

public class Switch
{
	public static void main(String[] args) 
	{
		String  day = "Sun";
		
		switch(day)
		{
		      case "Sun" :
		    	             System.out.println("Idli");
		    	             break;
		      
		      case "Mon" : 
		    	             System.out.println("Dosa");
		    	             break;
		    	             
		      case "Tuse"  :
		    	              System.out.println("Vadpav");
		    	             
		      case "Wen" :
		    	           System.out.println("Pohe");
		    	           break;
		    	   
		      case "Thar" :
		    	           System.out.println("Upma");
		    	           break;
		    	            
		      case "Fri" :
		    	          System.out.println("Dosa");
		    	          break;
		    	        
		      case "Sat" :
		    	             System.out.println("Masala Dosa");
		    	             break;
		    	             
		    	      
		     
		}
		
		
	}

}
